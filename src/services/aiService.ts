function detectTitle(lines: string[]): string {
  for (let i = 0; i < Math.min(15, lines.length); i++) {
    const line = lines[i].trim()
    if (line.length > 5 && line.length < 100 &&
        !/^[\d\s\.\-、,，・※*\-—]+$/.test(line) &&
        !line.endsWith('。') && !line.endsWith('、') && !line.endsWith('，') &&
        line.replace(/[「」『』【】]/g, '').length > 4) {
      return line
    }
  }
  return '学習ノート'
}

function isHeading(line: string): boolean {
  const t = line.trim()
  if (t.length < 3 || t.length > 80) return false
  if (t.endsWith('。') || t.endsWith('！') || t.endsWith('？')) return false
  if (/^[\d\s\.\-、,，・※\*\-—]+$/.test(t)) return false

  if (/^(\d+[\.\、\)]\s*|第[一二三四五六七八九十\d]+[章節节課回]\s*|Section\s*\d+|Chapter\s*\d+|■|□|◆|◇|▼|▽|◎|●|○)/.test(t)) return true
  if (t.length < 40 && !/[、，。「」『』]/.test(t)) return true
  if (/[概要紹介説明結論まとめ目標目的背景方法結果考察参考資料内容意味定義特徴分類種類意義課題展望].*[：:]?$/.test(t) && t.length < 60) return true
  if (/^[①②③④⑤⑥⑦⑧⑨⑩⑪⑫⑬⑭⑮⑯⑰⑱⑲⑳]/.test(t)) return true

  return false
}

function splitIntoSections(lines: string[]): Array<{ heading: string; content: string[] }> {
  const sections: Array<{ heading: string; content: string[] }> = []
  let currentHeading = ''
  let currentContent: string[] = []

  for (const line of lines) {
    const trimmed = line.trim()
    if (!trimmed) continue

    if (isHeading(trimmed)) {
      if (currentContent.length > 0) {
        sections.push({ heading: currentHeading || 'はじめに', content: currentContent })
      }
      currentHeading = trimmed
      currentContent = []
    } else {
      currentContent.push(line)
    }
  }

  if (currentContent.length > 0) {
    sections.push({ heading: currentHeading || '本文', content: currentContent })
  }

  if (sections.length === 0 && lines.length > 0) {
    sections.push({ heading: '本文', content: lines.slice(0, 200) })
  }

  return sections
}

function extractAutoKeyTerms(text: string): string[] {
  const terms = new Map<string, number>()

  const kanjiCompounds = text.match(/[一-鿿]{2,5}/g)
  if (kanjiCompounds) {
    for (const k of kanjiCompounds) {
      if (/^[年月日時分秒回度番号第次大小多少高低長短新旧好坏東西南北中上下左右内外前後]$/.test(k)) continue
      if (/^(これ|それ|あれ|この|その|あの|ここ|そこ|あそこ|こちら|そちら|あちら|どの|どこ|どちら)$/.test(k)) continue
      terms.set(k, (terms.get(k) || 0) + 1)
    }
  }

  const katakanaWords = text.match(/[゠-ヿ]{2,6}/g)
  if (katakanaWords) {
    for (const kw of katakanaWords) {
      if (/^[ァィゥェォッャュョヮヶー]+$/.test(kw)) continue
      terms.set(kw, (terms.get(kw) || 0) + 3)
    }
  }

  const brackets = text.match(/[「『（(]([^」』）)]{1,15})[」』）)]/g)
  if (brackets) {
    for (const b of brackets) {
      const inner = b.replace(/[「『（(」』）)\]]/g, '')
      if (inner.length >= 2 && inner.length <= 15) {
        terms.set(inner, (terms.get(inner) || 0) + 2)
      }
    }
  }

  return Array.from(terms.entries())
    .filter(([_, count]) => count >= 2)
    .sort((a, b) => b[1] - a[1])
    .slice(0, 20)
    .map(([term]) => term)
}

function extractContext(text: string, term: string): string {
  const idx = text.indexOf(term)
  if (idx === -1) return ''
  const start = Math.max(0, idx - 10)
  const end = Math.min(text.length, idx + term.length + 50)
  const snippet = text.slice(start, end).replace(/\n/g, ' ')
  return snippet
}

function scoreSentence(s: string): number {
  let score = 0
  if (s.length > 30 && s.length < 250) score += 1
  if (/\d/.test(s)) score += 1.5 // numbers = facts/data
  if (/[（(][^)）]+[)）]/.test(s)) score += 1 // definitions in parens
  if (/は|が|とは|である|重要な|ポイント|定義|意味|特徴|分類|種類|方法|結果|原因|目的|課題|必要|ポイント/.test(s)) score += 1
  if (/[！!?？]/.test(s)) score += 0.5 // engaging sentences
  // Very short lines are usually garbage
  if (s.length < 10) score -= 5
  // Lines that are just numbers/symbols
  if (/^[\d\s\.\-\/、,，・※\*]+$/.test(s)) score -= 5
  return score
}

function extractKeySentences(text: string, maxCount: number = 6): string[] {
  const raw = text.split(/[。！？.!?\n]/)
    .map(s => s.trim())
    .filter(s => s.length > 5)

  if (raw.length === 0) return []

  // Deduplicate
  const seen = new Set<string>()
  const unique = raw.filter(s => {
    const key = s.slice(0, 20)
    if (seen.has(key)) return false
    seen.add(key)
    return true
  })

  const scored = unique.map(s => ({ s, score: scoreSentence(s) }))
  scored.sort((a, b) => b.score - a.score)
  const top = scored.slice(0, maxCount).map(x => x.s)

  // Restore original order
  return top.sort((a, b) => text.indexOf(a) - text.indexOf(b))
}

function buildContentSummary(lines: string[]): string {
  const sections = splitIntoSections(lines)
  if (sections.length === 0) return ''

  let output = ''

  // Table of contents
  const headings = sections.map(s => s.heading).filter(h => h)
  if (headings.length > 1) {
    output += `**構成:** `
    output += headings.slice(0, 10).join(' → ')
    if (headings.length > 10) output += ` ...他${headings.length - 10}セクション`
    output += `\n\n`
  }

  for (const section of sections) {
    const body = section.content.join(' ')
    const keySentences = extractKeySentences(body, 5)

    if (keySentences.length === 0) continue

    output += `## ${section.heading}\n\n`
    for (const s of keySentences) {
      output += `- ${s}\n`
    }
    output += '\n'
  }

  return output
}

function generateSelfTestQuestions(text: string): string {
  const lines = text.split('\n').filter(l => l.trim())
  const sections = splitIntoSections(lines)
  const headings = sections.map(s => s.heading).filter(h => h && h !== '本文' && h !== 'はじめに')

  const qa: Array<{ q: string; a: string }> = []

  qa.push({
    q: 'この資料の主題は何ですか？重要なポイントを3つ挙げて説明してください。',
    a: pickAnswer(text, 3, 5)
  })

  if (headings.length > 0) {
    const secContent = sections.find(s => s.heading === headings[0])
    qa.push({
      q: `${headings[0]}の要点を自分の言葉で説明してみましょう。`,
      a: pickAnswer(secContent ? secContent.content.join(' ') : text, 2, 4)
    })
  }

  if (headings.length > 1) {
    qa.push({
      q: `${headings[1]}の内容と全体のテーマとの関連性を考察してください。`,
      a: pickAnswer(text, 2, 3)
    })
  }

  if (qa.length < 3) {
    qa.push({ q: 'この資料から学んだことを簡潔にまとめてください。', a: pickAnswer(text, 2, 4) })
  }

  return qa.map((item, i) => {
    let out = `${i + 1}. ${item.q}\n`
    if (item.a) {
      out += `   *解答例：${item.a}*\n`
    }
    return out
  }).join('\n')
}

function pickAnswer(text: string, minSentences: number, maxSentences: number): string {
  const sentences = text.split(/[。！？.!?]/)
    .map(s => s.trim())
    .filter(s => s.length > 15 && s.length < 200)

  if (sentences.length === 0) return ''

  const picked: string[] = []
  const step = Math.max(1, Math.floor(sentences.length / maxSentences))

  for (let i = 0; i < sentences.length && picked.length < maxSentences; i += step) {
    picked.push(sentences[i])
  }

  return picked.slice(0, maxSentences).join('。') + '。'
}

