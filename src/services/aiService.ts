function enrichText(text: string): string {
  let result = text

  for (const [kanji, reading] of Object.entries(READING_DICT)) {
    const escaped = kanji.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    const regex = new RegExp(`(?<![（(\\w])${escaped}(?![）)])`, 'g')
    result = result.replace(regex, `${kanji}（${reading}）`)
  }

  for (const [word, cn] of Object.entries(CN_ANNOTATION_DICT)) {
    const regex = new RegExp(`(?<![（(])${word}(?!（中文|[）)])`, 'g')
    let count = 0
    result = result.replace(regex, (match) => {
      count++
      if (count === 1) return `${match}（中文：${cn}）`
      return match
    })
  }

  return result
}

function generateStructuredNotes(fullText: string, pageCount: number): string {
  // Split and deduplicate lines (PPT PDFs often have repeated headers/footers)
  const allLines = fullText.split('\n').map(l => l.trim()).filter(l => l.length > 0)
  const seen = new Set<string>()
  const lines = allLines.filter(l => {
    const key = l.slice(0, 30)
    if (seen.has(key)) return false
    seen.add(key)
    return true
  })
  if (lines.length === 0) return '有効なテキストが見つかりませんでした。'

  const title = detectTitle(lines)
  const keyTerms = extractAutoKeyTerms(fullText)
  const selfTestQ = generateSelfTestQuestions(fullText)

  let output = `# ${title}\n\n`
  output += `**ページ数:** ${pageCount}ページ | **文字数:** ${fullText.length}文字\n\n---\n\n`

  // Key concepts section
  if (keyTerms.length > 0) {
    output += `## 核心キーワード\n\n`
    for (const term of keyTerms.slice(0, 12)) {
      const ctx = extractContext(fullText, term)
      output += `- **${term}**${ctx ? ` — ${ctx}` : ''}\n`
    }
    output += '\n---\n\n'
  }

  // Section-by-section refined content
  output += buildContentSummary(lines)

  // Self-test questions
  if (selfTestQ.trim()) {
    output += `---\n\n## 自己確認問題\n\n${selfTestQ}\n`
  }

  return output
}

// ========== Main entry point ==========

export function generateNotesLocal(pages: PDFPage[]): NotePage[] {
  const fullText = createFullTextStream(pages)
  const chunks = splitTextIntoChunks(fullText, 2000)

  const notePages: NotePage[] = []

  if (chunks.length === 0) {
    notePages.push({
      pageNumber: 1,
      originalContent: '',
      noteContent: '有効なコンテンツがありませんでした。'
    })
    return notePages
  }

  const structuredNotes = generateStructuredNotes(fullText, pages.length)

  notePages.push({
    pageNumber: 1,
    originalContent: fullText,
    noteContent: structuredNotes
  })

  return notePages
}

